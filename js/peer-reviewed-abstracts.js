// Render abstracts
function renderAbstracts() {
    console.log('üìÑ Rendering', peerReviewedAbstracts.length, 'peer-reviewed abstracts...');
    const abstractsGrid = document.getElementById('abstractsGrid');
    
    if (!abstractsGrid) {
        console.error('‚ùå Error: abstractsGrid element not found!');
        return false;
    }
    
    // Clear existing content
    abstractsGrid.innerHTML = '';
    
    // Force display to ensure grid is visible
    abstractsGrid.style.display = 'grid';
    abstractsGrid.style.opacity = '1';
    abstractsGrid.style.visibility = 'visible';
    
    peerReviewedAbstracts.forEach(abstract => {
        const abstractCard = document.createElement('a');
        abstractCard.href = abstract.link || '#';
        abstractCard.target = '_blank';
        abstractCard.rel = 'noopener noreferrer';
        abstractCard.className = 'abstract-card';
        
        // Force visibility on card
        abstractCard.style.opacity = '1';
        abstractCard.style.visibility = 'visible';
        
        // Create images section (after authors)
        let imagesHTML = '';
        if (abstract.images && abstract.images.length > 0) {
            imagesHTML = '<div class="abstract-images">';
            abstract.images.forEach((img, index) => {
                const singleClass = abstract.images.length === 1 ? ' single' : '';
                imagesHTML += `<img src="${img}" alt="karim serhan" class="abstract-image${singleClass}" loading="lazy">`;
            });
            imagesHTML += '</div>';
        }
        
        abstractCard.innerHTML = `
            <div class="abstract-content">
                <span class="abstract-number">Abstract ${abstract.number}</span>
                <h3 class="abstract-title">${abstract.title}</h3>
                <p class="abstract-authors">${abstract.authors}</p>
                ${imagesHTML}
                <div class="abstract-conference">
                    <i class="fas fa-calendar-alt"></i>
                    <span>${abstract.conference}</span>
                </div>
            </div>
            <div class="abstract-link-icon">
                <i class="fas fa-external-link-alt"></i>
            </div>
        `;
        
        abstractsGrid.appendChild(abstractCard);
    });
    
    console.log('‚úÖ Successfully rendered', abstractsGrid.children.length, 'abstract cards!');
    return true;
}

// Initialize with multiple fallbacks
function initializeAbstracts() {
    console.log('üìÑ Initializing peer-reviewed abstracts page...');
    
    // Try immediate rendering
    if (document.getElementById('abstractsGrid')) {
        renderAbstracts();
    } else {
        // Wait for element to be available
        console.log('‚è≥ Waiting for abstractsGrid element...');
        setTimeout(() => {
            if (document.getElementById('abstractsGrid')) {
                renderAbstracts();
            } else {
                console.error('‚ùå abstractsGrid element still not found after delay');
            }
        }, 500);
    }
}

// Initialize on DOM ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeAbstracts);
} else {
    // DOM is already ready
    initializeAbstracts();
}

// Also try to render when window loads (backup)
window.addEventListener('load', () => {
    const abstractsGrid = document.getElementById('abstractsGrid');
    if (abstractsGrid && abstractsGrid.children.length === 0) {
        console.log('üîÑ Backup render triggered from window.load');
        renderAbstracts();
    }
});

// Emergency fallback - render after 2 seconds if still empty
setTimeout(() => {
    const abstractsGrid = document.getElementById('abstractsGrid');
    if (abstractsGrid && abstractsGrid.children.length === 0) {
        console.log('üö® Emergency render triggered - grid was still empty after 2 seconds');
        renderAbstracts();
    }
}, 2000);

