// Render articles
function renderArticles() {
    console.log('ðŸ”¬ Rendering', researchArticles.length, 'research articles...');
    const articlesGrid = document.getElementById('articlesGrid');
    
    if (!articlesGrid) {
        console.error('âŒ Error: articlesGrid element not found!');
        return false;
    }
    
    // Clear existing content
    articlesGrid.innerHTML = '';
    
    // Force display to ensure grid is visible
    articlesGrid.style.display = 'grid';
    articlesGrid.style.opacity = '1';
    articlesGrid.style.visibility = 'visible';
    
    researchArticles.forEach(article => {
        const articleCard = document.createElement('a');
        articleCard.href = article.link || '#';
        articleCard.target = '_blank';
        articleCard.rel = 'noopener noreferrer';
        articleCard.className = 'article-card';
        
        // Force visibility on card
        articleCard.style.opacity = '1';
        articleCard.style.visibility = 'visible';
        
        // Create images section (after authors)
        let imagesHTML = '';
        if (article.images && article.images.length > 0) {
            imagesHTML = '<div class="article-images">';
            article.images.forEach((img, index) => {
                const singleClass = article.images.length === 1 ? ' single' : '';
                imagesHTML += `<img src="${img}" alt="karim sarhane" class="article-image${singleClass}" loading="lazy">`;
            });
            imagesHTML += '</div>';
        }
        
        articleCard.innerHTML = `
            <div class="article-content">
                <span class="article-number">Article ${article.number}</span>
                <h3 class="article-title">${article.title}</h3>
                <p class="article-authors">${article.authors}</p>
                ${imagesHTML}
                <div class="article-journal">
                    <i class="fas fa-book-medical"></i>
                    <span>${article.journal}</span>
                </div>
            </div>
            <div class="article-link-icon">
                <i class="fas fa-external-link-alt"></i>
            </div>
        `;
        
        articlesGrid.appendChild(articleCard);
    });
    
    console.log('âœ… Successfully rendered', articlesGrid.children.length, 'article cards!');
    return true;
}

// Initialize with multiple fallbacks
function initializeArticles() {
    console.log('ðŸ“„ Initializing research articles page...');
    
    // Add a small loading indicator
    const articlesGrid = document.getElementById('articlesGrid');
    if (articlesGrid) {
        articlesGrid.innerHTML = '<div style="text-align: center; padding: 3rem; color: #0891b2; font-size: 1.2rem;"><i class="fas fa-spinner fa-spin"></i> Loading articles...</div>';
    }
    
    // Try immediate rendering
    if (document.getElementById('articlesGrid')) {
        renderArticles();
    } else {
        // Wait for element to be available
        console.log('â³ Waiting for articlesGrid element...');
        setTimeout(() => {
            if (document.getElementById('articlesGrid')) {
                renderArticles();
            } else {
                console.error('âŒ articlesGrid element still not found after delay');
            }
        }, 500);
    }
}

// Initialize on DOM ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializeArticles);
} else {
    // DOM is already ready
    initializeArticles();
}

// Also try to render when window loads (backup)
window.addEventListener('load', () => {
    const articlesGrid = document.getElementById('articlesGrid');
    if (articlesGrid && articlesGrid.children.length === 0) {
        console.log('ðŸ”„ Backup render triggered from window.load');
        renderArticles();
    }
});

